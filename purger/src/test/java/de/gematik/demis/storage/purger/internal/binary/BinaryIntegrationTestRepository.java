import de.gematik.demis.storage.common.entity.BinaryEntity;
import jakarta.transaction.Transactional;
import java.time.OffsetDateTime;
import java.util.List;
import java.util.UUID;
import org.springframework.data.jpa.repository.Modifying;
import org.springframework.data.jpa.repository.Query;
import org.springframework.data.repository.CrudRepository;
import org.springframework.data.repository.query.Param;
import org.springframework.stereotype.Repository;

@Repository
interface BinaryIntegrationTestRepository extends CrudRepository<BinaryEntity, UUID> {
  @Modifying
  @Query("UPDATE binaries b SET b.lastUpdated = :timestamp WHERE b.id IN :ids")
  @Transactional
  void updateLastUpdatedTimestamps(
      @Param("timestamp") OffsetDateTime timestamp, @Param("ids") List<UUID> ids);
}
